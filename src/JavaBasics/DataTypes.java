package JavaBasics;

public class DataTypes {
public static void main(String args[]) {
	//datatypes :datatypes means how can we store data and in which format.
	//there are two types of datatypes primititve datatype and non primitive datatype
	//primitive-already defined    eg.boolean,char,byte,int,long,short,double,float
	//non primitive- user defined   eg.array,interfaces,classses
	//syntax: datatype variable name=value;
	
	int a=10;
	System.out.println(a);
	
	char k='p';
	System.out.println(k);
	
	
	byte b =123;
	System.out.println(b);
	 
	
	short s=23;
	System.out.println(s); //cant store large values
	
	boolean d=true;
	System.out.println(d);
	
	long f=23534546;
	System.out.println(f);  //for storing long values
	
	float h=34256789f;
	System.out.println(h);
	
	double j=234567;
	System.out.println(j);
	
	
	
}
}
